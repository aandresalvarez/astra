public enum PolicyDiagnosticSeverity: String, Codable, Sendable, Comparable, CaseIterable {
    case info
    case warning
    case blocked

    public static func < (lhs: PolicyDiagnosticSeverity, rhs: PolicyDiagnosticSeverity) -> Bool {
        let order: [PolicyDiagnosticSeverity] = [.info, .warning, .blocked]
        return order.firstIndex(of: lhs)! < order.firstIndex(of: rhs)!
    }
}

