public struct AgentPolicy: Codable, Equatable, Sendable {
    public var level: AgentPolicyLevel
    public var allowedTools: [String]
    public var askFirstTools: [String]
    public var deniedTools: [String]
    public var allowedShellPatterns: [String]
    public var askFirstShellPatterns: [String]
    public var deniedShellPatterns: [String]
    public var allowedURLPatterns: [String]
    public var deniedURLPatterns: [String]
    public var credentialLabels: [String]

    public init(
        level: AgentPolicyLevel,
        allowedTools: [String],
        askFirstTools: [String] = [],
        deniedTools: [String] = [],
        allowedShellPatterns: [String] = [],
        askFirstShellPatterns: [String] = [],
        deniedShellPatterns: [String] = [],
        allowedURLPatterns: [String] = [],
        deniedURLPatterns: [String] = [],
        credentialLabels: [String] = []
    ) {
        self.level = level
        self.allowedTools = Self.unique(allowedTools)
        self.askFirstTools = Self.unique(askFirstTools)
        self.deniedTools = Self.unique(deniedTools)
        self.allowedShellPatterns = Self.unique(allowedShellPatterns)
        self.askFirstShellPatterns = Self.unique(askFirstShellPatterns)
        self.deniedShellPatterns = Self.unique(deniedShellPatterns)
        self.allowedURLPatterns = Self.unique(allowedURLPatterns)
        self.deniedURLPatterns = Self.unique(deniedURLPatterns)
        self.credentialLabels = Self.unique(credentialLabels)
    }

    public static func preset(_ level: AgentPolicyLevel) -> AgentPolicy {
        switch level {
        case .locked:
            AgentPolicy(
                level: .locked,
                allowedTools: ["Read", "Glob", "Grep"],
                askFirstTools: [],
                deniedTools: ["Write", "Edit", "MultiEdit", "Bash", "WebFetch", "WebSearch"],
                deniedShellPatterns: ["*"],
                deniedURLPatterns: ["*"]
            )
        case .review:
            AgentPolicy(
                level: .review,
                allowedTools: ["Read", "Glob", "Grep"],
                askFirstTools: ["Write", "Edit", "MultiEdit", "Bash", "WebFetch", "WebSearch"],
                deniedShellPatterns: [
                    "rm:*",
                    "sudo:*",
                    "chmod:*",
                    "chown:*",
                    "git push:*",
                    "deploy:*",
                    "publish:*"
                ]
            )
        case .build:
            AgentPolicy(
                level: .build,
                allowedTools: ["Read", "Glob", "Grep", "Write", "Edit", "MultiEdit", "Bash"],
                askFirstTools: ["WebFetch", "WebSearch"],
                allowedShellPatterns: ["git:*", "swift:*", "./script/*", "xcodebuild:*"],
                deniedShellPatterns: ["sudo:*", "rm:*", "git push:*", "deploy:*", "publish:*"]
            )
        case .network:
            AgentPolicy(
                level: .network,
                allowedTools: ["Read", "Glob", "Grep", "Write", "Edit", "MultiEdit", "Bash", "WebFetch", "WebSearch"],
                askFirstTools: ["Bash(curl:*)"],
                allowedShellPatterns: ["git:*", "swift:*", "./script/*", "xcodebuild:*", "curl:*"],
                deniedShellPatterns: ["sudo:*", "rm:*", "git push:*", "deploy:*", "publish:*"]
            )
        case .autonomous:
            AgentPolicy(
                level: .autonomous,
                allowedTools: ["Read", "Glob", "Grep", "Write", "Edit", "MultiEdit", "Bash", "WebFetch", "WebSearch", "Agent"],
                allowedShellPatterns: ["*"],
                allowedURLPatterns: ["*"]
            )
        case .custom:
            AgentPolicy(
                level: .custom,
                allowedTools: ["Read", "Glob", "Grep"],
                askFirstTools: ["Write", "Edit", "MultiEdit", "Bash", "WebFetch", "WebSearch"]
            )
        }
    }

    public static func legacy(skipPermissions: Bool) -> AgentPolicy {
        preset(skipPermissions ? .autonomous : .review)
    }

    public func providerAllowedTools(requestedTools: [String]) -> [String] {
        switch level {
        case .locked, .review:
            return Self.unique(allowedTools)
        case .build, .network:
            return Self.unique(requestedTools + allowedTools).filter { !deniedTools.contains($0) }
        case .autonomous, .custom:
            let tools = requestedTools.isEmpty ? allowedTools : requestedTools + allowedTools
            return Self.unique(tools).filter { !deniedTools.contains($0) }
        }
    }

    public func applyingOneRunAllowedTools(_ approvedTools: [String]) -> AgentPolicy {
        let approved = Self.unique(approvedTools)
        guard !approved.isEmpty else { return self }

        var policy = self
        policy.allowedTools = Self.unique(policy.allowedTools + approved)
        policy.askFirstTools = policy.askFirstTools.filter { !approved.contains($0) }
        policy.deniedTools = policy.deniedTools.filter { !approved.contains($0) }
        return policy
    }

    private static func unique(_ values: [String]) -> [String] {
        Array(Set(values.filter { !$0.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty })).sorted()
    }
}

