import Foundation

public enum AgentPolicyLevel: String, Codable, CaseIterable, Identifiable, Sendable {
    case locked
    case review
    case build
    case network
    case autonomous
    case custom

    public var id: String { rawValue }

    public var displayName: String {
        switch self {
        case .locked: "Locked"
        case .review: "Review"
        case .build: "Build"
        case .network: "Network"
        case .autonomous: "Autonomous"
        case .custom: "Custom"
        }
    }

    public var shortDescription: String {
        switch self {
        case .locked:
            "Read and plan only."
        case .review:
            "Inspect work and ask before edits, broad shell, network, or credentials."
        case .build:
            "Edit scoped files and run approved build or test commands."
        case .network:
            "Use approved connectors and network destinations."
        case .autonomous:
            "Broad provider permissions for trusted or isolated work."
        case .custom:
            "Advanced user-defined ASTRA and provider rules."
        }
    }

    public var symbolName: String {
        switch self {
        case .locked: "lock.fill"
        case .review: "lock.shield.fill"
        case .build: "hammer.fill"
        case .network: "network"
        case .autonomous: "exclamationmark.shield.fill"
        case .custom: "slider.horizontal.3"
        }
    }

    public var isBroadPermissionMode: Bool {
        self == .autonomous
    }

    public static func normalized(_ rawValue: String?) -> AgentPolicyLevel {
        guard let rawValue,
              let level = AgentPolicyLevel(rawValue: rawValue.trimmingCharacters(in: .whitespacesAndNewlines).lowercased())
        else {
            return .review
        }
        return level
    }
}

