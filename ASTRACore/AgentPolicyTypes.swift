        self.settingsSummary = settingsSummary
        self.generatedConfigPreview = generatedConfigPreview
        self.enforcementTiers = enforcementTiers
        self.diagnostics = diagnostics
        self.usesBroadProviderPermissions = usesBroadProviderPermissions
